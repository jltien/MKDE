/*-----------------------------------------------------------------------------
* Check to see if long, double, and float data types are of the expected size *
-----------------------------------------------------------------------------*/
bool numDataBytesOK (void) {
	if ((sizeof(long)==4)&&(sizeof(double)==8)&&(sizeof(float)==4)&&(sizeof(char)==1)) {
		return true;
	}
	else {
		return false;
	}
}






